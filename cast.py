strnum = "1"
intnum = int(strnum)
print(intnum)       # 1
print(type(intnum)) # <class 'int'>


strnum = str(intnum)
print(strnum)       # 1
print(type(strnum)) # <class 'str'>

floatnum = float(intnum)
print(floatnum)       # 1.0
print(type(floatnum)) # <class 'float'>

